const params = new URLSearchParams(window.location.search);
const forcedLang = params.get("lang");
const langKey = "site-lang-v2";
let lang =
  forcedLang === "zh" || forcedLang === "en"
    ? forcedLang
    : localStorage.getItem(langKey) === "zh"
      ? "zh"
      : "en";

function renderStats(rows) {
  const root = document.getElementById("stats-grid");
  root.innerHTML = "";
  rows.forEach((row) => {
    const div = document.createElement("div");
    div.className = "stat";
    div.innerHTML = `<div class="k">${row.k}</div><div class="v">${row.v}</div>`;
    root.appendChild(div);
  });
}

function renderExperience(rows) {
  const root = document.getElementById("exp-list");
  root.innerHTML = "";
  rows.forEach((row) => {
    const item = document.createElement("article");
    item.className = "item";
    const bullets = row.bullets.map((b) => `<li>${b}</li>`).join("");
    item.innerHTML = `
      <h4>${row.role} | ${row.company}</h4>
      <p class="meta">${row.meta}</p>
      <ul>${bullets}</ul>
    `;
    root.appendChild(item);
  });
}

function renderProjects(rows) {
  const root = document.getElementById("project-list");
  root.innerHTML = "";
  rows.forEach((row) => {
    const card = document.createElement("article");
    card.className = "card";
    card.innerHTML = `<h4>${row.title}</h4><p>${row.body}</p>`;
    root.appendChild(card);
  });
}

function renderBulletList(id, rows) {
  const root = document.getElementById(id);
  root.innerHTML = "";
  rows.forEach((row) => {
    const li = document.createElement("li");
    li.textContent = row;
    root.appendChild(li);
  });
}

function renderSkillGroups(rows) {
  const root = document.getElementById("skills-groups");
  root.innerHTML = "";
  rows.forEach((row) => {
    const div = document.createElement("div");
    div.className = "skill-row";
    div.innerHTML = `<strong>${row.label}:</strong> ${row.value}`;
    root.appendChild(div);
  });
}

function render() {
  const t = content[lang];
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";

  Object.entries(ids).forEach(([key, id]) => {
    document.getElementById(id).textContent = t[key];
  });

  document.getElementById("cta-linkedin").textContent = t.ctaLinkedin;
  document.getElementById("cta-email").textContent = t.ctaEmail;
  document.getElementById("cta-copy").textContent = t.ctaCopy;

  renderStats(t.stats);
  renderExperience(t.experiences);
  renderProjects(t.projects);
  renderBulletList("edu-list", t.edu);
  renderBulletList("course-list", t.courses);
  renderBulletList("extra-list", t.extras);
  renderSkillGroups(t.skillGroups);

  document.getElementById("lang-switch").checked = lang === "zh";
}

document.getElementById("lang-switch").addEventListener("change", (e) => {
  lang = e.target.checked ? "zh" : "en";
  localStorage.setItem(langKey, lang);
  render();
});

document.getElementById("cta-copy").addEventListener("click", async () => {
  const t = content[lang];
  const toast = document.getElementById("copy-toast");
  try {
    await navigator.clipboard.writeText("shawn.xiang2@gmail.com");
    toast.textContent = t.copySuccess;
  } catch (err) {
    toast.textContent = "shawn.xiang2@gmail.com";
  }
  window.setTimeout(() => {
    toast.textContent = "";
  }, 1800);
});

render();
