let lang = "zh";

function fillList(containerId, entries) {
  const el = document.getElementById(containerId);
  el.innerHTML = "";
  entries.forEach((entry) => {
    const item = document.createElement("div");
    item.className = "item";

    const title = document.createElement("h3");
    title.textContent = entry.title;

    const desc = document.createElement("p");
    desc.textContent = entry.desc;

    item.appendChild(title);
    item.appendChild(desc);
    el.appendChild(item);
  });
}

function fillBulletList(containerId, rows) {
  const el = document.getElementById(containerId);
  el.innerHTML = "";
  rows.forEach((row) => {
    const li = document.createElement("li");
    li.textContent = row;
    el.appendChild(li);
  });
}

function render() {
  const t = data[lang];
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";
  document.getElementById("lang-toggle").textContent = t.langButton;
  document.getElementById("hero-tag").textContent = t.heroTag;
  document.getElementById("hero-title").textContent = t.heroTitle;
  document.getElementById("hero-summary").textContent = t.heroSummary;
  document.getElementById("goal-title").textContent = t.goalTitle;
  document.getElementById("goal-body").textContent = t.goalBody;
  document.getElementById("exp-title").textContent = t.expTitle;
  document.getElementById("project-title").textContent = t.projectTitle;
  document.getElementById("edu-title").textContent = t.eduTitle;
  document.getElementById("course-title").textContent = t.courseTitle;
  document.getElementById("skills-title").textContent = t.skillsTitle;
  document.getElementById("contact-title").textContent = t.contactTitle;
  document.getElementById("contact-body").textContent = t.contactBody;

  fillList("experience-list", t.experiences);
  fillList("project-list", t.projects);
  fillBulletList("edu-list", t.education);
  fillBulletList("course-list", t.courses);
  fillBulletList("skills-list", t.skills);
}

document.getElementById("lang-toggle").addEventListener("click", () => {
  lang = lang === "zh" ? "en" : "zh";
  render();
});

render();
